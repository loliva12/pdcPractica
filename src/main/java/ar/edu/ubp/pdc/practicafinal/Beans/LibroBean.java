package ar.edu.ubp.pdc.practicafinal.Beans;

public class LibroBean {
    int nroMaterial;
    String titulo;
    String autor;
    int votos;

    public int getNroMaterial() {
        return nroMaterial;
    }

    public void setNroMaterial(int nroMaterial) {
        this.nroMaterial = nroMaterial;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public int getVotos() {
        return votos;
    }

    public void setVotos(int votos) {
        this.votos = votos;
    }
}
