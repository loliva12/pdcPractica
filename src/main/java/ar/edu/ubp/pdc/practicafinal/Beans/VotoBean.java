package ar.edu.ubp.pdc.practicafinal.Beans;

public class VotoBean {
    String correo, telefono;
    int nroLegAlumno, nroMaterial;

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public int getNroLegAlumno() {
        return nroLegAlumno;
    }

    public void setNroLegAlumno(int nroLegAlumno) {
        this.nroLegAlumno = nroLegAlumno;
    }

    public int getNroMaterial() {
        return nroMaterial;
    }

    public void setNroMaterial(int nroMaterial) {
        this.nroMaterial = nroMaterial;
    }
}
